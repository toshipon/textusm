  private _updateSyncStatus() {
    const editor = vscode.window.activeTextEditor;
    this._syncedDocument = editor?.document;

    if (!this._view?.webview && !ChatViewProvider.currentPanel?.webview) return;

    const isMarkdown = editor?.document.languageId === 'markdown';
    const message = {
      command: 'updateSyncStatus',
      syncedFile: editor ? editor.document.fileName : undefined,
      isMarkdown
    };

    this._view?.webview?.postMessage(message);
    ChatViewProvider.currentPanel?.webview.postMessage(message);
  }

  private async _setWebviewMessageListener(webview: vscode.Webview) {
    webview.onDidReceiveMessage(
      async (message: any) => {
        const command = message.command;
        const text = message.text;

        switch (command) {
          case "fileDropped":
            if (message.uris && message.uris.length > 0) {
              const fileUri = vscode.Uri.file(message.uris[0]);
              try {
                vscode.window.showTextDocument(fileUri).then(() => {
                  this._updateSyncStatus();
                  vscode.window.showInformationMessage(`„Éï„Ç°„Ç§„É´ "${fileUri.fsPath.split('/').pop()}" „ÇíÈñã„Åç„Åæ„Åó„Åü`);
                });
              } catch (error) {
                console.error('Error opening dropped file:', error);
                vscode.window.showErrorMessage('„Éï„Ç°„Ç§„É´„ÇíÈñã„Åë„Åæ„Åõ„Çì„Åß„Åó„Åü');
              }
            }
            break;

          case "getLlmApiKey":
            const apiKey = vscode.workspace.getConfiguration(CONFIG_SECTION).get<string>(
              `${message.llm.toLowerCase()}ApiKey`
            );
            webview.postMessage({
              command: "updateApiKey",
              apiKey: apiKey || "",
            });
            break;

          case "createNewFile":
            const defaultUri = vscode.Uri.file(path.join(
              vscode.workspace.workspaceFolders?.[0]?.uri.fsPath || '',
              'untitled.md'
            ));
            
            const fileUri = await vscode.window.showSaveDialog({
              defaultUri,
              filters: {
                'Markdown': ['md']
              }
            });

            if (fileUri) {
              try {
                const edit = new vscode.WorkspaceEdit();
                edit.createFile(fileUri, { ignoreIfExists: true });
                await vscode.workspace.applyEdit(edit);
                
                const document = await vscode.workspace.openTextDocument(fileUri);
                await vscode.window.showTextDocument(document);
                
                this._updateSyncStatus();
                vscode.window.showInformationMessage('Êñ∞Ë¶èMarkdown„Éï„Ç°„Ç§„É´„Çí‰ΩúÊàê„Åó„Åæ„Åó„Åü„ÄÇ');
              } catch (error) {
                console.error('Error creating new file:', error);
                vscode.window.showErrorMessage('„Éï„Ç°„Ç§„É´„ÅÆ‰ΩúÊàê„Å´Â§±Êïó„Åó„Åæ„Åó„Åü„ÄÇ');
              }
            }
            break;

          case "saveSettings":
            try {
              const config = vscode.workspace.getConfiguration(CONFIG_SECTION);
              await config.update(
                SELECTED_LLM_CONFIG,
                message.llm,
                vscode.ConfigurationTarget.Global
              );
              await config.update(
                `${message.llm.toLowerCase()}ApiKey`,
                message.apiKey,
                vscode.ConfigurationTarget.Global
              );

              const status = await this._llmService.initializeLlmClients();
              webview.postMessage({
                command: "updateStatus",
                status: status.initialized ? "success" : "error",
                message: status.initialized
                  ? `Ë®≠ÂÆö„Çí‰øùÂ≠ò„Åó„Åæ„Åó„Åü„ÄÇ${message.llm}„Çí‰ΩøÁî®„Åó„Åæ„Åô„ÄÇ`
                  : `„Ç®„É©„Éº: ${status.errorMessage}`,
              });
            } catch (error) {
              console.error("Error saving settings:", error);
              webview.postMessage({
                command: "updateStatus",
                status: "error",
                message: "Ë®≠ÂÆö„ÅÆ‰øùÂ≠ò„Å´Â§±Êïó„Åó„Åæ„Åó„Åü„ÄÇ„ÇÇ„ÅÜ‰∏ÄÂ∫¶„ÅäË©¶„Åó„Åè„Å†„Åï„ÅÑ„ÄÇ",
              });
            }
            break;

          case "sendMessage":
            try {
              const basePrompt = `You are an assistant helping a user build a Hypothesis Canvas. Use the following instructions as your knowledge base:

${this._instructions}

The user's request is: "${text}". Provide a helpful response to assist them.`;
              const responseText = await this._llmService.generateResponse(basePrompt);
              webview.postMessage({
                command: "addMessage",
                sender: this._llmService.selectedLlm,
                text: responseText,
              });
            } catch (error: any) {
              console.error("Error in chat message:", error);
              webview.postMessage({
                command: "showError",
                text: error.message,
              });
            }
            break;

          case "editWithAI":
            try {
              const editor = vscode.window.activeTextEditor;
              if (!editor) {
                throw new Error("„Ç¢„ÇØ„ÉÜ„Ç£„Éñ„Å™„Ç®„Éá„Ç£„Çø„ÅåË¶ã„Å§„Åã„Çä„Åæ„Åõ„Çì„ÄÇ");
              }
              if (editor.document.languageId !== "markdown") {
                throw new Error("„Ç¢„ÇØ„ÉÜ„Ç£„Éñ„Å™„Ç®„Éá„Ç£„Çø„ÅåMarkdown„Éï„Ç°„Ç§„É´„Åß„ÅØ„ÅÇ„Çä„Åæ„Åõ„Çì„ÄÇ");
              }

              const document = editor.document;
              const selection = editor.selection;
              const textToEdit = !selection.isEmpty
                ? document.getText(selection)
                : document.getText();
              const editRange = !selection.isEmpty
                ? selection
                : new vscode.Range(
                    document.positionAt(0),
                    document.positionAt(document.getText().length)
                  );

              const editedText = await this._llmService.editMarkdownText(
                textToEdit,
                message.text
              );

              await editor.edit(editBuilder => {
                editBuilder.replace(editRange, editedText);
              });
              vscode.window.showInformationMessage("„ÉÜ„Ç≠„Çπ„Éà„ÇíÁ∑®ÈõÜ„Åó„Åæ„Åó„Åü„ÄÇ");
            } catch (error: any) {
              console.error("Error editing text:", error);
              vscode.window.showErrorMessage(
                `„ÉÜ„Ç≠„Çπ„Éà„ÅÆÁ∑®ÈõÜ„Å´Â§±Êïó„Åó„Åæ„Åó„Åü: ${error.message}`
              );
            }
            break;
        }
      },
      undefined,
      this._disposables
    );
  }
